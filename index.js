        // 1) Drop all active prompt injections.
        for (const key of [...activeInjectKeys]) setExtensionPrompt(key, '', extension_prompt_types.IN_CHAT, 0);
        activeInjectKeys.clear();

        // 2) Clear the current chat's maps (disable their lorebook entries first).
        const store = chatStore();
