/* ==================================================================
 *  Map Generators — SillyTavern extension
 *
 *  Embeds Watabou's procedural map generators, keeps a per-chat
 *  library of maps, and feeds a text description of each map to the
 *  AI (via a chat-bound lorebook entry OR a prompt injection) so the
 *  model "remembers" the places. State persists across reloads.
 *
 *  Design constraints (see plan):
 *   - Live generator runs in a modal POPUP (iframes are stripped from
 *     chat messages by DOMPurify, but popup content is not sanitized).
 *   - We can't read state back out of the cross-origin iframe, so OUR
 *     UI owns the seed/params; JSON/PNG enter via manual file upload.
 * ================================================================== */

